// Dos números, la suma y el resultado
let num11 = 13;
let num21 = 14;
let suma = num11 + num21;
document.write("<p>El resultado de sumar " + num11 + " y " + num21 + " es: " + suma + "</p>");


// Tres números, el promedio y el resultado
let num3 = 8;
let num4 = 29;
let num5 = 94;
let promedio = (num3 + num4 + num5) / 3;
document.write("<p> El promedio de los números " + num3 + " , " + num4 + " y " + num5 + " es: " + promedio + "</p>");

