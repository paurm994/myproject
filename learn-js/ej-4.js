let primerNumero = Number(prompt("Introduce el primer número"));
let segundoNumero = Number(prompt("Introduce el segundo número"));
let tercerNumero = Number(prompt("Introduce el tercer número"));
let media = (primerNumero + segundoNumero + tercerNumero) / 3;
document.write("La media de " + primerNumero + ", " + segundoNumero + " y " + tercerNumero + " es: " + media);