let x = 10;
document.write("<p>Iniciamos la variable con valor: " + x + "</p>");

// Primera forma de incrementar 3:
x = x + 3;
document.write("<p>Lo incrementamos en 3 de la primera forma: x = x + 3 ->" + x + "</p>");

// Volvemos a dejar la x con el valor inicial de 10:
x = 10
document.write("<p>Volvemos a dejar la x con el valor inicial: " + x + "</p>");

// Segunda forma de incrementar 3:
x += 3;
document.write("<p>Lo incrementamos en 3 de la segunda forma: x += 3 -> " + x + "</p>");
